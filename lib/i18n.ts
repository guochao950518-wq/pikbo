export const MESSAGES: Record<Locale, Dict> = { en, zh, ja, es };

/**
 * Translate a key. Supports `{name}` interpolation via vars.
 * Falls back: locale → en → key.
 */
export function translate(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>
): string {
  let s = MESSAGES[locale]?.[key] ?? MESSAGES.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replaceAll(`{${k}}`, String(v));
    }
  }
  return s;
}

export function isLocale(value: string | null | undefined): value is Locale {
  return !!value && (LOCALES as readonly string[]).includes(value);
}

/** Map browser language tags → supported locale (first visit only). */
export function detectLocaleFromNavigator(
  navLang?: string | null
): Locale | null {
  const raw = (navLang || "").toLowerCase();
  if (!raw) return null;
  if (raw.startsWith("zh")) return "zh";
  if (raw.startsWith("ja")) return "ja";
  if (raw.startsWith("es")) return "es";
  if (raw.startsWith("en")) return "en";
  return null;
}
